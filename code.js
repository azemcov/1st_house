var x = document.getElementById('x')
var y = document.getElementById('y')

function rx()
{
document.getElementById('box').style.transform = 'rotateX('+x.value+'deg) rotateY('+y.value+'deg)';
}

function ry()
{
document.getElementById('box').style.transform = 'rotateX('+x.value+'deg) rotateY('+y.value+'deg)';
}